//class creation
public class SwitchExample {  
public static void main(String[] args) {  
    //Declaring a variable for switch expression  
    int number=20;  
    //Switch expression  
    switch(number){  
    //Case statements  
    case 20: System.out.println("pass");  
    break;  
    case 10: System.out.println("retake");  
    break;  
    case 0: System.out.println("repeat");  
    break;  
    //Default case statement  
    default:System.out.println("discontinuity");  
    }  
}  
}  